//Custom Type
interface Data {
    id: number;
    name: string;
}

interface SelectProps {
    apiUrl: string;
    onSelect: (value: Data) => void;
    placeholder?: string;
}

const MySelectComponent: React.FC<SelectProps> = ({ apiUrl, onSelect, placeholder="Select an option"}) => {
  const [data, setData] = React.useState<Data[]>([]);
  const [value, setValue] = React.useState<string>(""); // State for selected value

  React.useEffect(() => {
    const fetchData = async () => {
      try {
        const response = await fetch(apiUrl);
        if (!response.ok) {
          throw new Error(`HTTP error! status: ${response.status}`);
        }
        const jsonData: Data[] = await response.json();
        setData(jsonData);
      } catch (error) {
        console.error("Failed to fetch data:", error);
      }
    };

    fetchData();
  }, [apiUrl]);

  const handleValueChange = (newValue: string) => {
    setValue(newValue); // Update selected value

    const selectedItem = data.find((item) => String(item.id) === newValue);
    if (selectedItem) {
      onSelect(selectedItem); // Call the onSelect with the selected item
    } else {
      onSelect({id: 0, name: ''}); //Handle deselect
    }
  };

  return (
    <Select onValueChange={handleValueChange}>
      <SelectTrigger className="w-[180px]">
        <SelectValue placeholder={placeholder} />
      </SelectTrigger>
      <SelectContent>
        {data.map((item) => (
          <SelectItem key={item.id} value={String(item.id)}>
            {item.name}
          </SelectItem>
        ))}
      </SelectContent>
    </Select>
  );
};

export {
    Select,
    SelectGroup,
    SelectValue,
    SelectTrigger,
    SelectContent,
    SelectLabel,
    SelectItem,
    SelectSeparator,
    SelectScrollUpButton,
    SelectScrollDownButton
  };